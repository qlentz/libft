#include "libft.h"

char	*ft_strchr(const char *str, int c)
{
	int	i;

	i = 0;
	if (!str)
		return (NULL);
	while (str[i] != (char)c)
	{
		if (!str[i++])
			return (NULL);
	}
	return ((char *)(str + i));
}
